package BinaryLiterals;

public class TestBinaryLiterals {
    public static void main(String[] args) {
        int bi = 0B101;  // binary
        int bi2 = 0101;  // octal
        int bi3 = 0X101;

        System.out.println(bi);
        System.out.println(bi2);
        System.out.println(bi3);
    }
}
